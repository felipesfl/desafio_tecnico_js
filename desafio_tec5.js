function calcularArea(base, altura){
    const area = base * altura
    return area
}

function calcularPerimetro(lado1, lado2, lado3, lado4){
    const perimetro = lado1 + lado2 + lado3 + lado4
    return perimetro
}

console.log(calcularArea(7.5, 7.5))
console.log(calcularPerimetro(8, 5, 6, 9))